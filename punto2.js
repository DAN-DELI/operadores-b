let numero1 =  parseInt(prompt("Ingresa el primer numero"));
let numero2 =  parseInt(prompt("Ingresa el segundo numero"));
let multiplicador = parseInt(prompt("Ingresa el tercer numero"));

let resta = numero1 - numero2;

let multi = resta * multiplicador;

console.log(`El resultado de (${numero1} - ${numero2}) * ${multiplicador} es ${multi}`);